package se.liu.ida.andze132.tddd78.javaproject;

/**
 * This enum contains all the directions for enemies to walk.
 */
public enum Direction {
    /**
     * Walk to the right.
     */
    RIGHT,
    /**
     * Walk to the left.
     */
    LEFT,
    /**
     * Walk upwards.
     */
    UPWARD,
    /**
     * Walk downwards.
     */
    DOWN,
    /**
     * Stand still.
     */
    STILL
}
